#include <iostream>

int main(){
    // declarando um array de inteiros com 5 elementos
    int array[5] = {10, 20, 30, 40, 50};

    // acessando e imprimindo os elementos do array
    printf("Elemento 1: %d\n", array [0]); // Primeiro elemento (indice 0)
    printf("Elemento 2: %d\n", array [1]); // Segundo elemento (indice 1)
    printf("Elemento 3: %d\n", array [2]); // Primeiro elemento (indice 2)
    printf("Elemento 4: %d\n", array [3]); // Primeiro elemento (indice 3)
    printf("Elemento 5: %d\n", array [4]); // Primeiro elemento (indice 4)

    return 0;
}